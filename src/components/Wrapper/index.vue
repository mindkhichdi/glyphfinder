<template>
  <div class="wrapper">
    <input
      class="wrapper__search"
      type="text"
      autofocus
      v-model.lazy="query"
      v-debounce="250"
    >

    <glyph-list :glyphs="results" />
  </div>
</template>

<script>
import lunr from 'lunr'
import debounce from 'v-debounce'
import SpatialNavigation from 'spatial-navigation-js'
import data from '@/data.yaml'
import GlyphList from '@/components/GlyphList'

export default {
  directives: { debounce },

  components: {
    GlyphList,
  },

  data() {
    return {
      query: null,
    }
  },

  computed: {
    results() {
      if (!this.query) {
        return data
      }

      return this.idx.query(query => {
        // exact match
        query.term(this.query, {
          fields: ['symbol', 'entities', 'tags'],
          boost: 3,
        })

        // first chars correct
        query.term(this.query, {
          fields: ['symbol', 'entities', 'tags'],
          boost: 2,
          wildcard: lunr.Query.wildcard.TRAILING,
        })

        // fuzzy
        query.term(this.query, {
          fields: ['symbol', 'entities', 'tags'],
          editDistance: 1,
          boost: 1,
        })

        query.term(this.query, {
          fields: ['description'],
          wildcard: lunr.Query.wildcard.TRAILING,
        })
      })
        .map(item => ({
          ...data.find(glyph => glyph.symbol === item.ref),
          score: item.score,
        }))
    },
  },

  mounted() {
    SpatialNavigation.init()
    SpatialNavigation.add({ selector: '[data-focusable]' })
    SpatialNavigation.makeFocusable()

    this.idx = lunr(function () {
      this.pipeline.remove(lunr.trimmer)
      this.pipeline.remove(lunr.stemmer)
      this.pipeline.remove(lunr.stopWordFilter)

      this.ref('symbol')
      this.field('symbol')
      this.field('entities')
      this.field('tags')
      this.field('description')

      data.forEach(function (glyph) {
        this.add(glyph)
      }, this)
    })
  },
}
</script>

